    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
                             
    String kode = jTextField1.getText().trim();
    String nama = jTextField3.getText().trim();
    String deskripsi = jTextField2.getText().trim();
    String kategori = jComboBox1.getSelectedItem().toString();
    double hargaJual, hargaBeli;
    int stok;

    // Validasi input
    if (kode.isEmpty() || nama.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Kode dan Nama barang wajib diisi!");
        return;
    }

    try {
        hargaJual = Double.parseDouble(jTextField4.getText().trim());
        hargaBeli = Double.parseDouble(jTextField6.getText().trim());
        stok = Integer.parseInt(jTextField7.getText().trim());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Format angka tidak valid!");
        return;
    }

    try (Connection conn = vapestore.koneksi.getKoneksi()) {
        String sql = "INSERT INTO products (product_code, product_name, product_image, "
                   + "product_category, product_price_s, product_price_b, product_stock) "
                   + "VALUES (?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, kode);
        pst.setString(2, nama);
        pst.setString(3, deskripsi);
        pst.setInt(4, kategori.equals("liquid") ? 1 : 2); // pastikan id kategori sesuai tabel produk_kategori
        pst.setDouble(5, hargaJual);
        pst.setDouble(6, hargaBeli);
        pst.setInt(7, stok);

        int rows = pst.executeUpdate();

        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Produk berhasil disimpan!");
            
            // Tutup dialog Tambah Produk
            this.dispose();

            // Buka halaman Data Produk setelah disimpan
            Data_produk dp = new Data_produk();
            dp.setVisible(true);
            dp.pack();
            dp.setLocationRelativeTo(null);
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan produk!");
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Kesalahan database: " + e.getMessage());
    }


    }//GEN-LAST:event_jButton2ActionPerformed


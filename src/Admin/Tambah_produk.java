    private void generateKodeBarang() {
    try (Connection conn = vapestore.koneksi.getKoneksi()) {
        // Ambil kode terakhir berdasarkan angka setelah "BRG"
        String sql = "SELECT product_code FROM products " +
                     "WHERE product_code LIKE 'BRG%' " +
                     "ORDER BY CAST(SUBSTRING(product_code, 4) AS UNSIGNED) DESC LIMIT 1";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        String newCode = "BRG001"; // default jika tabel kosong
        if (rs.next()) {
            String lastCode = rs.getString("product_code");
            if (lastCode != null) {
                try {
                    // Ambil angka terakhir dari kode
                    String numberPart = lastCode.replaceAll("[^0-9]", "");
                    int number = Integer.parseInt(numberPart);
                    number++; // tambah 1
                    newCode = String.format("BRG%03d", number); // BRG + 3 digit
                } catch (NumberFormatException e) {
                    // Jika format salah, tetap BRG001
                    newCode = "BRG001";
                }
            }
        }

        jTextField1.setText(newCode); // tampilkan di field KODE BARANG
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal generate kode barang: " + e.getMessage());
    }
}




